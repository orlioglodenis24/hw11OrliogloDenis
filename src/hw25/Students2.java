package hw25;

public class Students2 {

    String name;
    int age;

    public Students2(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String toString() {
        return name;
    }

    public Integer getAge() {
        return age;
    }
}
